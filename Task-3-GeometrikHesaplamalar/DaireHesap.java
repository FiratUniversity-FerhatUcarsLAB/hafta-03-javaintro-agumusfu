public class DaireHesap {
    public static void main(String[] args) {
        double r = 5.5;
        double pi = 3.14159;
        //alan hesapla
        double alan = pi * r * r;
        //çevre hesapla
        double cevre = 2 * pi * r;
        //Yazdırma
        System.out.println(" Yaricap : " + r );
        System.out.println(" Alan : " + alan );
        System.out.println(" Cevre : " + cevre );
    }
}
